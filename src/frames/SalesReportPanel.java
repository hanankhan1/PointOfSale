    private void dailyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dailyActionPerformed
        // TODO add your handling code here:

        int day = (Integer) date.getValue();
        int month = (Integer) this.month.getValue();
        int year = (Integer) this.year.getValue();
        if (day != 0 && month != 0 && year != 0) {
            LocalDate selectedDate = LocalDate.of(year, month, day);
            double dailyProfit = Store.dalyPro(selectedDate);
            reportArea.setText("");
            for (Sale sale : Store.getAllSales()) {
                if (sale.getDate().equals(selectedDate)) {
                    Product product = sale.getProduct();
                    int quantitySold = sale.getQuantitySold();
                    double sellingPrice = product.getSellingp();
                    double totalPrice = sellingPrice * quantitySold;
                    reportArea.setText(String.format("Product: %s, Quantity Sold: %d, Total Price: %.2f\n",
                            product.getName(), quantitySold, totalPrice));
                }
            }
            reportArea.setText("\nDaily Profit for " + selectedDate + ": " + String.format("%.2f", dailyProfit));
        } else {
            Store.showMsgError("Enter the date first..!");
        }
    }//GEN-LAST:event_dailyActionPerformed

