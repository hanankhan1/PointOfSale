    private void monthlyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_monthlyActionPerformed
        // TODO add your handling code here:
        int month = (Integer) this.month.getValue();
        int year = (Integer) this.year.getValue();
        if (month != 0 && year != 0) {
            double monthlypro = Store.monthlyPro(month, year);
            reportArea.setText("");

            for (Sale sale : Store.getAllSales()) {
                if (sale.getDate().getMonthValue() == month && sale.getDate().getYear() == year) {
                    Product product = sale.getProduct();
                    int quantitySold = sale.getQuantitySold();
                    double sellingPrice = product.getSellingp();
                    double totalPrice = sellingPrice * quantitySold;
                
                    reportArea.setText(String.format("Product: %s, Quantity Sold: %d, Total Price: %.2f\n",
                            product.getName(), quantitySold, totalPrice));
                }
            }

            
            reportArea.setText("\nMonthly Profit for " + month + "/" + year + ": " + String.format("%.2f", monthlypro));
        } else {
            Store.showMsgError("Enter the date first..!");
        }

    }//GEN-LAST:event_monthlyActionPerformed

